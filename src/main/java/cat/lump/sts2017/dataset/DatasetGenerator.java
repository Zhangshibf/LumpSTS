  /**
   * Assumes that an empty answer is equivalent to y (yes).
   * 
   * @param option 
   *          a String containing y, n, or empty
   * @return
   *          true if empty or y
   */
  private boolean yesOrEmpty(String option) {
    if (option.isEmpty() || option.equals("y")) {
      return true;
    }
    return false;
  }
  
  
